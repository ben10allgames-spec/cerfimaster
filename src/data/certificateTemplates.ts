import { CertificateTemplate, CertificateElement } from '@/types/certificate';
import { v4 as uuidv4 } from 'uuid';

export interface PresetTemplate {
  id: string;
  name: string;
  description: string;
  category: 'completion' | 'participation' | 'achievement' | 'internship' | 'appreciation' | 'professional';
  preview: string;
  template: Omit<CertificateTemplate, 'id' | 'createdAt' | 'updatedAt'>;
}

const createTextElement = (
  text: string,
  x: number,
  y: number,
  options: Partial<CertificateElement> = {}
): Omit<CertificateElement, 'id'> => ({
  type: 'text',
  x,
  y,
  text,
  fontFamily: 'Inter',
  fontSize: 24,
  fontWeight: 'normal',
  fill: '#000000',
  textAlign: 'center',
  ...options,
});

const createPlaceholderElement = (
  key: string,
  x: number,
  y: number,
  options: Partial<CertificateElement> = {}
): Omit<CertificateElement, 'id'> => ({
  type: 'placeholder',
  x,
  y,
  text: `{{${key}}}`,
  placeholderKey: key,
  fontFamily: 'Inter',
  fontSize: 32,
  fontWeight: 'bold',
  fill: '#1e3a5f',
  textAlign: 'center',
  ...options,
});

const createShapeElement = (
  x: number,
  y: number,
  width: number,
  height: number,
  options: Partial<CertificateElement> = {}
): Omit<CertificateElement, 'id'> => ({
  type: 'shape',
  x,
  y,
  width,
  height,
  shapeType: 'rectangle',
  fill: '#e2e8f0',
  stroke: '#94a3b8',
  strokeWidth: 1,
  ...options,
});

const createLineElement = (
  x: number,
  y: number,
  width: number,
  options: Partial<CertificateElement> = {}
): Omit<CertificateElement, 'id'> => ({
  type: 'line',
  x,
  y,
  width,
  height: 2,
  fill: '#c9a227',
  strokeWidth: 2,
  ...options,
});

